from time import sleep

print("Algoritmo de funcionamiento de Semáforo")

delayGreen = 5
delayYellow = 3
delayRed = 5

while True:
    print("Verde")
    sleep(delayGreen)
    print("Amarillo")
    sleep(delayYellow)
    print("Rojo")
    sleep(delayRed)



